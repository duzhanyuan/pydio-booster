package pydioworker

import (
	pydiolog "github.com/pydio/pydio-booster/log"
)

var log *pydiolog.Logger

func init() {
	log = pydiolog.New(pydiolog.GetLevel(), "[worker] ", pydiolog.Ldate|pydiolog.Ltime|pydiolog.Lmicroseconds)
}

// Job interface
type Job interface {
	Do() error
}

// Worker represents the worker that executes the job
type Worker struct {
	WorkerPool chan chan Job
	JobChannel chan Job
	quit       chan bool
}

// NewWorker to execute jobs
func NewWorker(workerPool chan chan Job) Worker {
	return Worker{
		WorkerPool: workerPool,
		JobChannel: make(chan Job),
		quit:       make(chan bool)}
}

// Start method starts the run loop for the worker, listening for a quit channel in
// case we need to stop it
func (w Worker) Start() {
	go func() {
		for {
			// register the current worker into the worker queue.
			w.WorkerPool <- w.JobChannel

			select {
			case job := <-w.JobChannel:
				// we have received a work request.
				if err := job.Do(); err != nil {
					log.Errorf("Worker's job failed: %s", err.Error())
				}

			case <-w.quit:
				// we have received a signal to stop
				return
			}
		}
	}()
}

// Stop signals the worker to stop listening for work requests.
func (w Worker) Stop() {
	go func() {
		w.quit <- true
	}()
}
